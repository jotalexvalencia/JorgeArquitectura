package beans;

public class BeanJorge {

	private int x;
	private int y;
	private int suma;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getSuma() {
		return suma;
	}

	public void setSuma() {
		this.suma = this.x+this.y;
	}

	

}
